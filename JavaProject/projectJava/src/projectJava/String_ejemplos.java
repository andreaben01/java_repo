package projectJava;

public class String_ejemplos {

	public static void main(String[] args) {

		String x1 = "hola";
		String x2 = "chau";
		
		System.out.println("concatenar");
		String x3 = x1 + x2;
		String x4 = x1 + " " + x2;
		System.out.println(x3);
		System.out.println(x4);


		System.out.println("comparaciones");
		System.out.println(x1.equals(x2));
		System.out.println(x1.equals(x1));

		
		System.out.println("minus y mayus");
		String unTexto = "laLa";
		System.out.println(unTexto.toLowerCase());
		System.out.println(unTexto.toUpperCase());


		
		
	
		
	}

}
